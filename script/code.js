   // Function to greet with a given name
function greet(name) {
    console.log("Hello " + name);
}
// Function to multiply two numbers
function multiply(a, b) {
    console.log(a * b);
}
// Function to multiply a number by five and log the result
function tableOfFive(number) {
    let result = number * 5;
    console.log(result);
}
// Example usage:
greet("Alice");           // Logs: Hello Alice
multiply(3, 4);           // Logs: 12
tableOfFive(7);           // Logs: 35